const frutas = ['banana', 'maçã', 'laranja', 'uva', 'melancia'];

// Acessando um elemento do array
console.log(frutas[0]); // banana

// Alterando um elemento do array
frutas[0] = 'morango';
console.log(frutas[0]); // morango

// Adicionando um elemento ao array
frutas.push('abacaxi');
console.log(frutas); // ['morango', 'maçã', 'laranja', 'uva', 'melancia', 'abacaxi']