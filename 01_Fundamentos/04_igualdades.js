const idade = 30;

console.log(idade == 30); //Verifica se a idade é igual a 30
console.log(idade == 31); //Verifica se a idade é igual a 31
console.log(idade > 30); //Verifica se a idade é maior que 30
console.log(idade < 30); //Verifica se a idade é menor que 30
console.log(idade >= 30); //Verifica se a idade é maior ou igual a 30
console.log(idade <= 30); //Verifica se a idade é menor ou igual a 30
console.log(idade != 30); //Verifica se a idade é diferente de 30
console.log(idade === 30); //Verifica se a idade é estritamente igual a 30
console.log(idade !== 30); //Verifica se a idade é estritamente diferente de 30
