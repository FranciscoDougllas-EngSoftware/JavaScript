//Utilizando o switch

const a = 4;

switch(a){
    case 1:
        console.log("A = 1");
        break;
    case 2:
        console.log("A = 2");
        break;
    case 3:
        console.log("A = 3");
        break;
    case 4:
        console.log("A = 4");
        break;
    case 5:
        console.log("A = 5");
        break;
    default:
        console.log("A = Default");
        break;
}